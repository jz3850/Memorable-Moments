function leapMonth(year) {
  return lunarInfo[year - 1900] & 0xf
}

function leapDays(year) {
  if (leapMonth(year)) {
    return (lunarInfo[year - 1900] & 0x10000) ? 30 : 29
  }
  return 0
}

function monthDays(year, month) {
  return (lunarInfo[year - 1900] & (0x10000 >> month)) ? 30 : 29
}

function lunarYearDays(year) {
  let sum = 348
  for (let i = 0x8000; i > 0x8; i >>= 1) {
    if (lunarInfo[year - 1900] & i) sum += 1
  }
  return sum + leapDays(year)
}

function solarToLunar(date) {
  const baseDate = new Date(1900, 0, 31)
  let offset = Math.floor((date - baseDate) / 86400000)

  let year = 1900
  while (year < 2100 && offset >= lunarYearDays(year)) {
    offset -= lunarYearDays(year)
    year++
  }

  const leap = leapMonth(year)
  let isLeap = false
  let month = 1

  while (month <= 12) {
    let days

    if (isLeap) {
      days = leapDays(year)
    } else {
      days = monthDays(year, month)
    }

    if (offset < days) break

    offset -= days

    if (leap === month && !isLeap) {
      isLeap = true
    } else {
      if (isLeap) isLeap = false
      month++
    }
  }

  return {
    lunarYear: year,
    lunarMonth: month,
    lunarDay: offset + 1,
    isLeap
  }
}

function lunarToSolar(year, month, day, isLeap = false) {
  let offset = 0

  for (let y = 1900; y < year; y++) {
    offset += lunarYearDays(y)
  }

  const leap = leapMonth(year)

  for (let m = 1; m < month; m++) {
    offset += monthDays(year, m)

    if (m === leap) {
      offset += leapDays(year)
    }
  }

  if (isLeap && leap === month) {
    offset += monthDays(year, month)
  }

  offset += day - 1

  return new Date(1900, 0, 31 + offset)
}

